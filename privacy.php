      <section>
        <h3 id="collection">1. Quais dados coletamos sobre você e para qual finalidade?</h3>
        <p>Nosso site coleta e utiliza alguns dados pessoais seus, de forma a viabilizar a prestação de serviços e aprimorar a experiência de uso.</p>
        <h4 id="collection_1">1.1. Dados pessoais fornecidos pelo titular</h4>
        <p>• Dados cadastrais como nome, endereço, telefone, e CPF/CNPJ. Têm por objetivo possibilitar a emissão de notas fiscais e seu registro é obrigação legal da empresa para a realização de vendas.</p>
        <p>• Dados referentes a formas de pagamento, como dados bancários ou informações de cartão de crédito. Como no item anterior, são necessários para a efetivação dos pedidos.</p>

        <h4 id="collection_2">1.2. Dados pessoais coletados automaticamente</h4>
        <p>• A GMS não coleta nenhum dado automaticamente. Todos os dados coletados são explicitamente fornecidos pelo usuário.</p>

        <h3 id="como_coletamos">2. Como coletamos os seus dados?</h3>
        <p>Todos os dados que coletamos são explicitamente fornecidos pelo cliente no momento do cadastro e, após a aprovação do orçamento, no pagamento do pedido. Não coletamos nenhum dado de forma automática.</p>
        <h4 id="como_coletamos_1">2.1. Consentimento</h4>
        <p>
          É a partir do seu consentimento que tratamos os seus dados pessoais. O consentimento é a manifestação livre, informada e inequívoca pela qual você autoriza a GMS a tratar seus dados.
          Assim, em consonância com a Lei Geral de Proteção de Dados, seus dados só serão coletados, tratados e armazenados mediante prévio e expresso consentimento.
          O seu consentimento será obtido de forma específica para cada finalidade acima descrita, evidenciando o compromisso de transparência e boa-fé GMS para com seus usuários/clientes, seguindo as regulações legislativas pertinentes.
          Ao utilizar os serviços da GMS e fornecer seus dados pessoais, você está ciente e consentindo com as disposições desta Política de Privacidade, além de conhecer seus direitos e como exercê-los.
          A qualquer tempo e sem nenhum custo, você poderá revogar seu consentimento.
          É importante destacar que a revogação do consentimento para o tratamento dos dados implica na impossibilidade da realização de novos pedidos.
        </p>
