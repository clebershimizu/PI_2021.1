        <h3 id="armazenamento">4. Como e por quanto tempo seus dados serão armazenados?</h3>
        <p>Seus dados pessoais coletados pela GMS serão utilizados e armazenados durante o tempo necessário para a prestação do serviço ou para que as finalidades elencadas na presente Política de Privacidade sejam atingidas, considerando os direitos dos titulares dos dados e dos controladores.
          De modo geral, seus dados serão mantidos enquanto a relação contratual entre você e a GMS perdurar. Como todos os dados coletados são estritamente necessários para o cumprimento legal dos responsabilidades do negócio, são válidas as hipóteses previstas no artigo 16 lei geral de proteção de dados, a saber:</p>
        <p>I – cumprimento de obrigação legal ou regulatória pelo controlador;</p>

        <p>Isto é, informações pessoais sobre você que sejam imprescindíveis para o cumprimento de determinações legais, judiciais e administrativas e/ou para o exercício do direito de defesa em processos judiciais e administrativos serão mantidas, a despeito da exclusão dos demais dados.
          O armazenamento de dados coletados pela GMS reflete o nosso compromisso com a segurança e privacidade dos seus dados. Empregamos medidas e soluções técnicas de proteção aptas a garantir a confidencialidade, integridade e inviolabilidade dos seus dados. Além disso, também contamos com medidas de segurança apropriadas aos riscos e com controle de acesso às informações armazenadas.
        </p>

